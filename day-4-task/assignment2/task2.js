// JS Program to divide & multiply two no's

function multiplyFun()
{
        num1 = document.getElementById("fNumber").value;
        num2 = document.getElementById("sNumber").value;
        document.getElementById("result").innerHTML = num1 * num2;
}

function divideFun()
{
        num1 = document.getElementById("fNumber").value;
        num2 = document.getElementById("sNumber").value;
        document.getElementById("result").innerHTML = num1 / num2;
}